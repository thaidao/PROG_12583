# sum grade of 4 students

# Declare variables
fStuGrade1 = 8.8
fStuGrade2 = 3
fStuGrade3 = 7
fStuGrade4 = 9.9

# Calculate the sume of 4 students
fSumGrade = fStuGrade1\
        + fStuGrade2\
        + fStuGrade3\
        + fStuGrade4

# Print it out the final result
print("Sum grade of 4 student is:", fSumGrade, end="\n\r")